@MainActor
private final class ManualClipboardDelayScheduler {
    private(set) var scheduledDurations: [Duration] = []
    private var actions: [() -> Void] = []

    func schedule(
        after delay: Duration,
        action: @escaping @MainActor () -> Void
    ) -> Task<Void, Never> {
        scheduledDurations.append(delay)
        actions.append(action)
        return Task {}
    }

    func runNext() {
        guard actions.isEmpty == false else {
            Issue.record("Expected scheduled delay action")
            return
        }

        let action = actions.removeFirst()
        action()
    }
}

@MainActor
private final class RecordingClipboardPasteboardClient: ClipboardPasteboardClient {
    var changeCount: Int = 0
    var lastWrittenTypes: [String] = []

    func snapshot() -> ClipboardPasteboardSnapshot {
        ClipboardPasteboardSnapshot(
            changeCount: changeCount,
            availableTypes: [],
            dataByType: [:],
            fileURLs: []
        )
    }

    func write(items: [NSPasteboardItem]) throws {
        lastWrittenTypes = items.flatMap { item in
            item.types.map(\.rawValue)
        }
        changeCount += 1
    }
}

@MainActor
private final class ScriptedTextClipboardPasteboardClient: ClipboardPasteboardClient {
    private(set) var changeCount: Int = 0
    private var text = ""

    func copy(text: String) {
        self.text = text
        changeCount += 1
    }

    func snapshot() -> ClipboardPasteboardSnapshot {
        ClipboardPasteboardSnapshot(
            changeCount: changeCount,
            availableTypes: ["public.utf8-plain-text"],
            dataByType: ["public.utf8-plain-text": Data(text.utf8)],
            fileURLs: []
        )
    }

    func write(items: [NSPasteboardItem]) throws {}
}

@MainActor
private final class FailingClipboardPasteboardClient: ClipboardPasteboardClient {
    let writeError: Error
    var changeCount: Int = 0

    init(writeError: Error) {
        self.writeError = writeError
    }

    func snapshot() -> ClipboardPasteboardSnapshot {
        ClipboardPasteboardSnapshot(
            changeCount: changeCount,
            availableTypes: [],
            dataByType: [:],
            fileURLs: []
        )
    }

    func write(items: [NSPasteboardItem]) throws {
        throw writeError
    }
}

