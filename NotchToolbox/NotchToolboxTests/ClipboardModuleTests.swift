    @Test func liveClientDropsOversizedRepresentationData() {
        let pasteboard = NSPasteboard.withUniqueName()
        pasteboard.clearContents()
        let item = NSPasteboardItem()
        item.setData(
            Data(repeating: 0xAB, count: 2048),
            forType: NSPasteboard.PasteboardType("com.notch.test.blob")
        )
        item.setString("hello", forType: .string)
        pasteboard.writeObjects([item])

        let client = LiveClipboardPasteboardClient(
            pasteboard: pasteboard,
            maxInlinePayloadBytes: 1024
        )
        let snapshot = client.snapshot()

        // The oversized representation is dropped from memory, the small one and
        // the full type list are kept.
        #expect(snapshot.dataByType["com.notch.test.blob"] == nil)
        #expect(snapshot.dataByType["public.utf8-plain-text"] != nil)
        #expect(snapshot.availableTypes.contains("com.notch.test.blob"))
    }

